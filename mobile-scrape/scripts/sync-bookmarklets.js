#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..', '..');
const outFile = path.resolve(__dirname, '..', 'www', 'js', 'payloads.js');
const shimFile = path.resolve(__dirname, 'iab-chrome-shim.js');

// Each target's payload is the matching extension's `config.js` (which sets
// `globalThis.TOK_CONFIG`) concatenated with the per-page scraper, wrapped in
// an IIFE so the config's `var` declarations don't leak onto the host page.
// The chrome extensions inject these as two scripts in the same isolated
// world; here we deliver them as one string the FAB can eval inside the IAB.
// We also prepend a shim that recreates `chrome.runtime.sendMessage` —
// the scrapers use it to delegate Sheets/Graylog POSTs to the extension
// background worker, which doesn't exist in a Cordova webview.
const SOURCES = {
  creator:  { config: 'extension-agency/config.js',  script: 'extension-agency/scrape-creator.js'  },
  sellers:  { config: 'extension-agency/config.js',  script: 'extension-agency/scrape-sellers.js'  },
  live:     { config: 'extension-seller/config.js',  script: 'extension-seller/scrape-live.js'     },
  streamer: { config: 'extension-seller/config.js',  script: 'extension-seller/scrape-streamer.js' },
};

if (!fs.existsSync(shimFile)) {
  console.error(`[sync-bookmarklets] missing shim ${shimFile}`);
  process.exit(1);
}
const shim = fs.readFileSync(shimFile, 'utf8');

const payloads = {};
for (const [key, { config, script }] of Object.entries(SOURCES)) {
  const cfgPath = path.join(repoRoot, config);
  const scrPath = path.join(repoRoot, script);
  for (const p of [cfgPath, scrPath]) {
    if (!fs.existsSync(p)) {
      console.error(`[sync-bookmarklets] missing ${p}`);
      process.exit(1);
    }
  }
  const cfg = fs.readFileSync(cfgPath, 'utf8');
  const scr = fs.readFileSync(scrPath, 'utf8');
  payloads[key] = `(function(){\n${shim}\n${cfg}\n${scr}\n})();\n`;
}

const banner = `// AUTO-GENERATED by scripts/sync-bookmarklets.js — do not edit.\n` +
               `// Sourced from repo-root bookmarklet-*.js at ${new Date().toISOString()}.\n`;
const body = `window.PAYLOADS = ${JSON.stringify(payloads, null, 2)};\n`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, banner + body, 'utf8');
console.log(`[sync-bookmarklets] wrote ${outFile} (${Object.keys(payloads).length} payloads)`);
